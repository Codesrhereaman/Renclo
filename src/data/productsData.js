// Export by category for easy filtering
export const getMenProducts = () => RENTAL_ITEMS.filter(item => item.category === 'men');
export const getWomenProducts = () => RENTAL_ITEMS.filter(item => item.category === 'women');
export const getAccessoriesProducts = () => RENTAL_ITEMS.filter(item => item.category === 'accessories');

// Get product by ID
export const getProductById = (id) => RENTAL_ITEMS.find(item => item.id === id);

// Search products
export const searchProducts = (query) => {
  const lowerQuery = query.toLowerCase();
  return RENTAL_ITEMS.filter(item =>
    item.name.toLowerCase().includes(lowerQuery) ||
    item.category.toLowerCase().includes(lowerQuery)
  );
};

// Get featured products (top rated)
export const getFeaturedProducts = () => {
  return RENTAL_ITEMS.filter(item => item.rating >= 4.7).slice(0, 6);
};

// Get products by price range
export const getProductsByPriceRange = (minPrice, maxPrice) => {
  return RENTAL_ITEMS.filter(item => item.rentalPrice >= minPrice && item.rentalPrice <= maxPrice);
};

// Get in-stock products
export const getInStockProducts = () => {
  return RENTAL_ITEMS.filter(item => item.inStock);
};

// Get top-rated products
export const getTopRatedProducts = (limit = 5) => {
  return [...RENTAL_ITEMS].sort((a, b) => b.rating - a.rating).slice(0, limit);
};
